package com.v12.project;

public class NoticeImageDTO {
	
	private int notice_no;
    private int game_no;
    private String notice_img_no;
    private String notice_img_name;
    private String notice_img_path;
    
    
	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public int getGame_no() {
		return game_no;
	}
	public void setGame_no(int game_no) {
		this.game_no = game_no;
	}
	public String getNotice_img_no() {
		return notice_img_no;
	}
	public void setNotice_img_no(String notice_img_no) {
		this.notice_img_no = notice_img_no;
	}
	public String getNotice_img_name() {
		return notice_img_name;
	}
	public void setNotice_img_name(String notice_img_name) {
		this.notice_img_name = notice_img_name;
	}
	public String getNotice_img_path() {
		return notice_img_path;
	}
	public void setNotice_img_path(String notice_img_path) {
		this.notice_img_path = notice_img_path;
	}
   
	

}
